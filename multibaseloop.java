public class multibaseloop { 
public static void main(String[] args) { 
for (int b=1; b<=8; b++) {
for (int a = 1; a <= 4; a++) {
System.out.print(" ");
}
System.out.print("|");
}
System.out.println();
for (int i=1; i<=8; i++) {
for (int j=1; j<=4; j++) {
System.out.print(j+"");}
System.out.print("0");
}
}
}
